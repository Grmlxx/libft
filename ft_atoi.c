#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	int	resl;

	sign = 1;
	resl = 0;
	while (*nptr == ' ' || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (*nptr >= '0' && *nptr <= '9')
	{
		resl = (*nptr - '0') + (resl * 10);
		nptr++;
	}
	return (resl * sign);
}
